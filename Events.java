
public class Events {
	
	String event;
	int eventValue;
	
	public Events (String event, int eventValue) {
		this.event = event;
		this.eventValue = eventValue;
	}

	public String getEvent() {
		return event;
	}

	public void setEvent(String event) {
		this.event = event;
	}

	public int getEventValue() {
		return eventValue;
	}

	public void setEventValue(int eventValue) {
		this.eventValue = eventValue;
	}

}
